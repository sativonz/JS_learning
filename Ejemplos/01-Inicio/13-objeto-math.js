    console.log(Math);
    console.log(Math.PI);

    //Valor absoluto
    console.log(Math.abs(-7.8));

    //Redondear -> a lo alto
    console.log(Math.ceil(7.2));
    //Redondear -> a la baja
    console.log(Math.floor(7.8));
    //Redondear -> redondeo clásico
    console.log(Math.round(7.49));

    //Raiz cuadrada
    console.log(Math.sqrt(81));

    //Elevar potenccias
    console.log(Math.pow(2, 5));

    //Para comprobar si es un número positivo, negativo o 0 -> -1 0 1
    console.log(Math.sign(-0.3)); // -1 0 1

    //Valor aleatorio comprendido entre 0 y 1
    console.log(Math.random());

     //Valor aleatorio comprendido entre 0 y 1000
    console.log(Math.round(Math.random() * 1000)); 
